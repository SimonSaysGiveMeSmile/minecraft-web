/**
 * Render a string into one combined bitmap (rows of '#'/'.').
 * Unknown characters are skipped, spaces become a 3-column gap.
 */
export function textToBitmap(text: string): string[] {
  const rows = ['', '', '', '', '']
  for (const char of text.toUpperCase()) {
    if (char === ' ') {
      for (let r = 0; r < 5; r++) rows[r] += '...'
      continue
    }
    const glyph = FONT[char]
    if (!glyph) continue
    for (let r = 0; r < 5; r++) {
      rows[r] += (rows[r] ? '.' : '') + glyph[r]
    }
  }
  return rows[0].length ? rows : []
}

export function isRenderable(char: string) {
  return char === ' ' || FONT[char.toUpperCase()] !== undefined
}
